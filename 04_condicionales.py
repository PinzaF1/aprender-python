# 04_condicionales.py - Aprendiendo condicionales en Python

# Los condicionales permiten tomar decisiones
edad = 18

if edad >= 18:
    print("Eres mayor de edad")
else:
    print("Eres menor de edad")

# Condicional con elif
nota = 85

if nota >= 90:
    print("Excelente")
elif nota >= 80:
    print("Muy bien")
elif nota >= 70:
    print("Bien")
else:
    print("Necesitas mejorar")

print("¡Fin del programa!")